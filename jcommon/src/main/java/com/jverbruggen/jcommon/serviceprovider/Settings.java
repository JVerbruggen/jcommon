package com.jverbruggen.jcommon.serviceprovider;

public record Settings(String prefix, boolean broadcastMode, boolean debugMode) {

    @Override
    public boolean equals(Object obj) {
        if (obj == this) return true;
        if (obj == null || obj.getClass() != this.getClass()) return false;
        var that = (Settings) obj;
        return this.broadcastMode == that.broadcastMode &&
                this.debugMode == that.debugMode;
    }

    @Override
    public String toString() {
        return "Settings[" +
                "broadcastMode=" + broadcastMode + ", " +
                "debugMode=" + debugMode + ']';
    }

    public static Settings defaultValues(){
        return new Settings("jcommon", false, false);
    }
}
